// Helper function to generate document number based on company and branch
export const generateDocumentNumber = (companyId: string, branchId: string, runningNumber: number): string => {
  const now = new Date();
  const yymm = `${String(now.getFullYear()).slice(-2)}${String(now.getMonth() + 1).padStart(2, '0')}`;
  const running = String(runningNumber).padStart(5, '0');
  
  // Document prefix mapping by company and branch
  const prefixMap: Record<string, Record<string, string>> = {
    'BPK': { 'default': 'BPKRS' },
    'VPA': { 'default': 'VPARS' },
    'LAC': {
      'br-LAC-01': 'LRARS',  // สาขารามอินทรา
      'br-LAC-02': 'LSVRS',  // สาขาสุขุมวิท
    },
    'ICCK': {
      'br-ICCK-01': 'ICKRS',  // สาขาหนองแขม
      'br-ICCK-02': 'IKKRS',  // สาขากิ่งแก้ว
    },
  };
  
  let prefix = 'RS'; // default fallback
  
  if (prefixMap[companyId]) {
    if (prefixMap[companyId][branchId]) {
      prefix = prefixMap[companyId][branchId];
    } else if (prefixMap[companyId]['default']) {
      prefix = prefixMap[companyId]['default'];
    }
  }
  
  return `${prefix}-${yymm}${running}`;
};

